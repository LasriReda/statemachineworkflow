package com.reda.workflow.configuration;


public class StateConfig {
    private String id;
    private String type; // INITIAL, END, or null

    // Getters and setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}